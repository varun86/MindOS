/**
 * Install a `mindos` shell command that delegates to the bundled (or dev) CLI.
 * Does not use npm global install — writes ~/.mindos/bin/mindos and optionally
 * appends PATH to ~/.zshrc (etc.).
 */
import { app, dialog, BrowserWindow, Notification } from 'electron';
import path from 'path';
import {
  mkdirSync,
  writeFileSync,
  chmodSync,
  existsSync,
  readFileSync,
  appendFileSync,
} from 'fs';
import { getDefaultBundledMindOsDirectory } from './mindos-runtime-path';
import { resolveCliPath } from './mindos-runtime-layout';
import { getDesktopConfigDir, getDesktopHome } from './desktop-home';
import semver from 'semver';

const SHELL_MARKER = '# MindOS Desktop — CLI (mindos)';

function shimDir(): string {
  return path.join(getDesktopConfigDir(), 'bin');
}

function shimPath(): string {
  return path.join(shimDir(), process.platform === 'win32' ? 'mindos.cmd' : 'mindos');
}

/** Git Bash / MSYS entry point on Windows — bash ignores PATHEXT, so `mindos` needs a real sh script. */
function shShimPath(): string {
  return path.join(shimDir(), 'mindos');
}

/**
 * Resolve the best CLI entry point: prefer cached runtime (Core Hot Update)
 * over bundled if it has a newer version.
 */
function resolveCliJs(): string | null {
  const bundledRoot = getDefaultBundledMindOsDirectory();
  let bundledCli: string | null = null;
  let bundledVersion: string | null = null;
  if (bundledRoot) {
    const p = resolveCliPath(bundledRoot);
    if (existsSync(p)) {
      bundledCli = p;
      try {
        const pkg = JSON.parse(readFileSync(path.join(bundledRoot, 'package.json'), 'utf-8'));
        bundledVersion = typeof pkg.version === 'string' ? pkg.version : null;
      } catch { /* ignore */ }
    }
  }

  // Check cached runtime (installed by Core Hot Update)
  const cachedRoot = path.join(getDesktopConfigDir(), 'runtime');
  const cachedCli = path.join(cachedRoot, 'bin', 'cli.js');
  if (existsSync(cachedCli)) {
    let cachedVersion: string | null = null;
    try {
      const pkg = JSON.parse(readFileSync(path.join(cachedRoot, 'package.json'), 'utf-8'));
      cachedVersion = typeof pkg.version === 'string' ? pkg.version : null;
    } catch { /* ignore */ }

    // Prefer cached if it has a newer version than bundled
    if (cachedVersion && semver.valid(cachedVersion)) {
      if (!bundledVersion || !semver.valid(bundledVersion) || semver.gt(cachedVersion, bundledVersion)) {
        return cachedCli;
      }
    }
  }

  if (bundledCli) return bundledCli;

  if (!app.isPackaged) {
    const devRoot = path.resolve(app.getAppPath(), '..');
    const dev = resolveCliPath(devRoot);
    return existsSync(dev) ? dev : null;
  }
  return null;
}

/** Safe single-quoted literal for POSIX sh. */
function shellSingleQuote(s: string): string {
  return `'${s.replace(/'/g, `'\"'\"'`)}'`;
}

function writeUnixShim(cliJs: string): void {
  const cliLit = shellSingleQuote(cliJs);
  const script = `#!/bin/sh
# Generated by MindOS Desktop — overwritten on each app launch.

# Guard against deleted working directory (e.g. AI Agent in temp dir).
# Only cd away when the cwd is actually gone — an unconditional cd would
# break relative-path arguments like \`mindos import ./notes.md\`.
if ! pwd >/dev/null 2>&1; then cd "$HOME" 2>/dev/null || true; fi

CLI=${cliLit}

# If target CLI is missing (e.g. Desktop .app deleted), try npm global fallback
if [ ! -f "$CLI" ]; then
  NPM_ROOT=\$(npm root -g 2>/dev/null)
  NPM_CLI="\${NPM_ROOT}/@geminilight/mindos/bin/cli.js"
  if [ -f "$NPM_CLI" ]; then
    CLI="$NPM_CLI"
  else
    echo "mindos: MindOS Desktop has been removed and no npm global install found." >&2
    echo "" >&2
    echo "  To reinstall CLI:      npm install -g @geminilight/mindos" >&2
    echo "  To clean up residuals:  bash ~/.mindos/uninstall.sh" >&2
    echo "  To reinstall Desktop:   https://mindos.app/download" >&2
    exit 127
  fi
fi

if [ -x "$HOME/.mindos/node/bin/node" ]; then
  exec "$HOME/.mindos/node/bin/node" "$CLI" "$@"
fi
if command -v node >/dev/null 2>&1; then
  exec node "$CLI" "$@"
fi
echo "mindos: Node.js not found. Open MindOS Desktop once (it installs Node under ~/.mindos/node), or install Node 18+." >&2
exit 127
`;
  const out = shimPath();
  mkdirSync(shimDir(), { recursive: true });
  writeFileSync(out, script, 'utf-8');
  try {
    chmodSync(out, 0o755);
  } catch {
    /* ignore */
  }
}

/**
 * Escape a value for use inside a quoted `set "VAR=value"` with delayed
 * expansion DISABLED: only `%` needs doubling (parse-time expansion); `^` and
 * `!` are literal inside quotes. Double quotes cannot be represented safely.
 */
export function escapeCmdSetValue(value: string): string {
  if (value.includes('"')) {
    throw new Error('Invalid Windows path for cmd script: double quote is not allowed');
  }
  return value.replace(/%/g, '%%');
}

/**
 * Express a path under the user's home as %USERPROFILE%-relative.
 *
 * cmd.exe parses batch files in the OEM/ANSI codepage (CP936 etc.), not the
 * UTF-8 we write — a literal `C:\Users\张三\...` would be misread and every
 * `if exist` check would fail. %USERPROFILE% keeps the file pure ASCII for
 * home-relative paths (which covers ~/.mindos and per-user NSIS installs).
 * Non-home paths (dev repos, per-machine installs) stay absolute: still
 * correct for ASCII paths, and the known residual risk for non-ASCII ones.
 */
export function toCmdUserProfilePath(p: string, home: string): string {
  const norm = path.win32.normalize(p);
  const homeNorm = path.win32.normalize(home).replace(/[\\/]+$/, '');
  if (norm.toLowerCase().startsWith(`${homeNorm.toLowerCase()}\\`)) {
    return `%USERPROFILE%${escapeCmdSetValue(norm.slice(homeNorm.length))}`;
  }
  return escapeCmdSetValue(norm);
}

/** Same idea for the Git Bash shim: "$HOME" + forward-slash remainder. */
export function toShUserProfilePath(p: string, home: string): string {
  const norm = path.win32.normalize(p);
  const homeNorm = path.win32.normalize(home).replace(/[\\/]+$/, '');
  if (norm.toLowerCase().startsWith(`${homeNorm.toLowerCase()}\\`)) {
    return `"$HOME"${shellSingleQuote(norm.slice(homeNorm.length).replace(/\\/g, '/'))}`;
  }
  return shellSingleQuote(norm.replace(/\\/g, '/'));
}

export function buildWindowsCmdShimScript(cliJs: string, home: string): string {
  const cliRef = toCmdUserProfilePath(cliJs, home);
  const nodeRef = toCmdUserProfilePath(path.win32.join(path.win32.normalize(home), '.mindos', 'node', 'node.exe'), home);
  return [
    '@echo off',
    'rem Generated by MindOS Desktop - overwritten on each app launch.',
    'rem Delayed expansion stays DISABLED: with it enabled, the `!`/`^` in user',
    'rem arguments (%*) are eaten by the delayed-expansion phase.',
    'setlocal disabledelayedexpansion',
    `set "CLI=${cliRef}"`,
    `set "NODE_PRIV=${nodeRef}"`,
    'if exist "%CLI%" goto have_cli',
    'for /f "delims=" %%i in (\'npm root -g 2^>nul\') do set "NPM_CLI=%%i\\@geminilight\\mindos\\bin\\cli.js"',
    'if not exist "%NPM_CLI%" goto no_cli',
    'set "CLI=%NPM_CLI%"',
    'goto have_cli',
    '',
    ':no_cli',
    'echo mindos: MindOS Desktop has been removed. Reinstall: npm install -g @geminilight/mindos >&2',
    'echo To clean up residuals: "%USERPROFILE%\\.mindos\\uninstall.bat" >&2',
    'exit /b 127',
    '',
    ':have_cli',
    'if not exist "%NODE_PRIV%" goto use_path_node',
    '"%NODE_PRIV%" "%CLI%" %*',
    'exit /b %errorlevel%',
    '',
    ':use_path_node',
    'where node >nul 2>&1 || (echo mindos: Node.js not found. Open MindOS Desktop or install Node 18+. >&2 & exit /b 127)',
    'node "%CLI%" %*',
    'exit /b %errorlevel%',
    '',
  ].join('\r\n');
}

export function buildWindowsShShimScript(cliJs: string, home: string): string {
  const cliRef = toShUserProfilePath(cliJs, home);
  const nodeRef = toShUserProfilePath(path.win32.join(path.win32.normalize(home), '.mindos', 'node', 'node.exe'), home);
  return `#!/bin/sh
# Generated by MindOS Desktop — Git Bash / MSYS entry point on Windows.
# bash does not use PATHEXT, so mindos.cmd alone is invisible to it.

if ! pwd >/dev/null 2>&1; then cd "$HOME" 2>/dev/null || true; fi

CLI=${cliRef}
NODE_PRIV=${nodeRef}

if [ ! -f "$CLI" ]; then
  NPM_ROOT=$(npm root -g 2>/dev/null)
  NPM_CLI="\${NPM_ROOT}/@geminilight/mindos/bin/cli.js"
  if [ -f "$NPM_CLI" ]; then
    CLI="$NPM_CLI"
  else
    echo "mindos: MindOS Desktop has been removed and no npm global install found." >&2
    echo "  To reinstall CLI: npm install -g @geminilight/mindos" >&2
    exit 127
  fi
fi

if [ -x "$NODE_PRIV" ]; then
  exec "$NODE_PRIV" "$CLI" "$@"
fi
if command -v node >/dev/null 2>&1; then
  exec node "$CLI" "$@"
fi
echo "mindos: Node.js not found. Open MindOS Desktop once, or install Node 18+." >&2
exit 127
`;
}

function writeWindowsShim(cliJs: string): void {
  const home = getDesktopHome();
  mkdirSync(shimDir(), { recursive: true });
  writeFileSync(shimPath(), buildWindowsCmdShimScript(cliJs, home), 'utf-8');
  // LF on purpose — sh scripts with CRLF break in Git Bash
  writeFileSync(shShimPath(), buildWindowsShShimScript(cliJs, home), 'utf-8');
  try { chmodSync(shShimPath(), 0o755); } catch { /* NTFS — Git Bash runs it via shebang regardless */ }
}

/** PATH entry written to the registry — %USERPROFILE% form survives profile moves and stays ASCII. */
const WINDOWS_PATH_ENTRY = '%USERPROFILE%\\.mindos\\bin';

/**
 * PowerShell that prepends ~/.mindos/bin to the user PATH via the raw
 * registry API. [Environment]::GetEnvironmentVariable expands REG_EXPAND_SZ
 * on read, so a naive read-modify-write permanently flattens entries like
 * %JAVA_HOME%\bin — DoNotExpandEnvironmentNames preserves them, ExpandString
 * keeps the value kind, and the explicit WM_SETTINGCHANGE broadcast replaces
 * the one SetEnvironmentVariable would have sent.
 */
export function buildWindowsPathRegistryScript(): string {
  return [
    `$bin = '${WINDOWS_PATH_ENTRY}'`,
    `$key = [Microsoft.Win32.Registry]::CurrentUser.CreateSubKey('Environment')`,
    `$cur = [string]$key.GetValue('Path', '', [Microsoft.Win32.RegistryValueOptions]::DoNotExpandEnvironmentNames)`,
    `$binExpanded = [Environment]::ExpandEnvironmentVariables($bin).TrimEnd('\\')`,
    `$has = $false`,
    `foreach ($part in ($cur -split ';')) {`,
    `  if (-not $part) { continue }`,
    `  $norm = [Environment]::ExpandEnvironmentVariables($part).TrimEnd('\\')`,
    `  if ($norm -ieq $binExpanded) { $has = $true; break }`,
    `}`,
    `if ($has) { 'PRESENT'; exit 0 }`,
    `$new = if ($cur) { $bin + ';' + $cur } else { $bin }`,
    `$key.SetValue('Path', $new, [Microsoft.Win32.RegistryValueKind]::ExpandString)`,
    `Add-Type -Namespace MindOS -Name Native -MemberDefinition '[DllImport("user32.dll", SetLastError = true, CharSet = CharSet.Auto)] public static extern System.IntPtr SendMessageTimeout(System.IntPtr hWnd, uint Msg, System.UIntPtr wParam, string lParam, uint fuFlags, uint uTimeout, out System.UIntPtr lpdwResult);'`,
    `[System.UIntPtr]$result = [System.UIntPtr]::Zero`,
    `[MindOS.Native]::SendMessageTimeout([System.IntPtr]0xffff, 0x1A, [System.UIntPtr]::Zero, 'Environment', 2, 5000, [ref]$result) | Out-Null`,
    `'CHANGED'`,
  ].join('\n');
}

/**
 * Append ~/.mindos/bin to user PATH on Windows via the raw registry API.
 * Returns true if PATH was modified.
 */
function appendMindosBinToWindowsPath(): boolean {
  try {
    const { execFileSync } = require('child_process');
    // -EncodedCommand sidesteps every cmd/PowerShell quoting pitfall
    const encoded = Buffer.from(buildWindowsPathRegistryScript(), 'utf16le').toString('base64');
    const out = String(execFileSync('powershell.exe', [
      '-NoProfile', '-NonInteractive', '-ExecutionPolicy', 'Bypass', '-EncodedCommand', encoded,
    ], { encoding: 'utf-8', timeout: 10000, windowsHide: true, stdio: ['pipe', 'pipe', 'pipe'] }));
    const changed = out.includes('CHANGED');
    if (changed) console.info(`[MindOS] Added ${WINDOWS_PATH_ENTRY} to Windows user PATH`);
    return changed;
  } catch (err) {
    console.warn('[MindOS] Could not update Windows PATH:', err instanceof Error ? err.message : err);
    return false;
  }
}

/**
 * Append ~/.mindos/bin to PATH in ALL relevant shell rc files (idempotent per file).
 *
 * AI Agents (Claude Code, Cursor, etc.) run commands via non-interactive shells:
 * - Non-interactive zsh reads ~/.zshenv (NOT ~/.zshrc)
 * - Non-interactive bash reads ~/.bashrc (some systems) or nothing
 * - Fish reads ~/.config/fish/conf.d/*.fish always
 *
 * We must write to ALL relevant files so the PATH is available everywhere.
 * Returns true if we appended to at least one file.
 */
/** True when an UNCOMMENTED line references .mindos/bin (sh and fish syntax alike). */
export function hasActiveMindosPathLine(content: string): boolean {
  return content.split('\n').some((line) => {
    const trimmed = line.trim();
    return !trimmed.startsWith('#') && trimmed.includes('.mindos/bin');
  });
}

function appendMindosBinToShellRc(): boolean {
  if (process.platform === 'win32') return appendMindosBinToWindowsPath();
  const home = getDesktopHome();
  const block = `\n${SHELL_MARKER}\nexport PATH="$HOME/.mindos/bin:$PATH"\n`;

  // Files to write, in order. "mustCreate" means create the file if it doesn't exist.
  interface RcTarget { name: string; mustCreate: boolean }

  const targets: RcTarget[] =
    process.platform === 'darwin'
      ? [
          { name: '.zshenv',       mustCreate: true },   // non-interactive zsh (AI Agents)
          { name: '.zshrc',        mustCreate: false },   // interactive zsh (user terminal)
          { name: '.bashrc',       mustCreate: true },    // non-interactive bash (AI Agents)
          { name: '.bash_profile', mustCreate: false },   // login bash
        ]
      : [
          { name: '.bashrc',  mustCreate: true },    // bash (interactive + non-interactive)
          { name: '.zshenv',  mustCreate: true },    // non-interactive zsh (AI Agents)
          { name: '.zshrc',   mustCreate: false },   // interactive zsh
          { name: '.profile', mustCreate: false },   // login shell fallback
        ];

  let appended = false;

  for (const { name, mustCreate } of targets) {
    const full = path.join(home, name);
    const fileExists = existsSync(full);

    // Skip non-existent files unless we must create them
    if (!fileExists && !mustCreate) continue;

    // Skip if already has our marker OR legacy CLI marker (idempotent).
    // A raw '.mindos/bin' mention only counts when it appears on an
    // uncommented line — a commented-out leftover must not block reinstall.
    if (fileExists) {
      try {
        const content = readFileSync(full, 'utf-8');
        if (content.includes(SHELL_MARKER) || content.includes('# MindOS CLI')
            || hasActiveMindosPathLine(content)) continue;
      } catch { continue; }
    }

    try {
      appendFileSync(full, block, 'utf-8');
      appended = true;
    } catch {
      // Best effort — don't fail the whole install for one file
    }
  }

  // Fish shell support: write fish-syntax config if fish config dir exists
  try {
    const fishConfD = path.join(home, '.config', 'fish', 'conf.d');
    if (existsSync(fishConfD)) {
      const fishFile = path.join(fishConfD, 'mindos.fish');
      const fishMarker = '# MindOS Desktop — CLI (mindos)';
      const fishBlock = `${fishMarker}\nif not contains "$HOME/.mindos/bin" $PATH\n  set -gx PATH "$HOME/.mindos/bin" $PATH\nend\n`;
      let needsWrite = !existsSync(fishFile);
      if (!needsWrite) {
        const fc = readFileSync(fishFile, 'utf-8');
        needsWrite = !fc.includes(fishMarker) && !fc.includes('# MindOS CLI') && !hasActiveMindosPathLine(fc);
      }
      if (needsWrite) {
        writeFileSync(fishFile, fishBlock, 'utf-8');
        appended = true;
      }
    }
  } catch { /* best effort */ }

  return appended;
}

