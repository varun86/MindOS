/** Generate ~/.mindos/uninstall.sh/.bat for cleanup after app deletion */
function writeUninstallScript(): void {
  const home = getDesktopHome();
  if (process.platform === 'win32') {
    const scriptPath = path.join(home, '.mindos', 'uninstall.bat');
    if (existsSync(scriptPath)) return;
    try {
      mkdirSync(path.dirname(scriptPath), { recursive: true });
      writeFileSync(scriptPath, buildWindowsUninstallScript(), 'utf-8');
    } catch { /* best effort */ }
    return;
  }

  const script = buildUnixUninstallScript();

  const scriptPath = path.join(home, '.mindos', 'uninstall.sh');
  if (existsSync(scriptPath)) return; // idempotent — only create once
  try {
    mkdirSync(path.dirname(scriptPath), { recursive: true });
    writeFileSync(scriptPath, script, 'utf-8');
    chmodSync(scriptPath, 0o755);
  } catch { /* best effort */ }
}

const NOTIFY_FLAG = 'desktop-cli-path-notify-v1';

function notifyFlagPath(): string {
  return path.join(getDesktopConfigDir(), NOTIFY_FLAG);
}

function showMessageBoxSafe(
  parent: BrowserWindow | null | undefined,
  options: Electron.MessageBoxOptions,
): void {
  const win = parent && !parent.isDestroyed() ? parent : null;
  if (win) void dialog.showMessageBox(win, options);
  else void dialog.showMessageBox(options);
}

/** One-time hint after ~/.mindos/bin is appended to shell rc — banner, not a blocking dialog. */
function notifyPathAppendedOnce(): void {
  if (existsSync(notifyFlagPath())) return;
  try {
    writeFileSync(notifyFlagPath(), new Date().toISOString(), 'utf-8');
  } catch {
    /* still try notification */
  }

  const zh = app.getLocale()?.toLowerCase().startsWith('zh');
  const title = zh ? 'mindos 命令行已就绪' : 'mindos CLI is ready';
  const body = zh
    ? '已写入 ~/.mindos/bin 并加入 PATH。请新开终端后运行 mindos --help'
    : '~/.mindos/bin added to PATH. Open a new terminal, then run mindos --help';

  if (Notification.isSupported()) {
    try {
      new Notification({ title, body }).show();
      return;
    } catch {
      /* fall through */
    }
  }
  console.info(`[MindOS] ${title} — ${body}`);
}

/**
 * Sync shim on disk. Call on each launch so CLI tracks the current .app location.
 * Optionally append PATH once; first PATH change triggers a one-time OS notification (no modal).
 */
export function ensureMindosCliShim(opts?: {
  /** default true on macOS/Linux */
  appendPath?: boolean;
}): { ok: boolean; error?: string; pathAppended?: boolean } {
  const cliJs = resolveCliJs();
  if (!cliJs) {
    const msg =
      'Bundled MindOS CLI not found (mindos-runtime/bin/cli.js). Reinstall the app or run from a built repo.';
    console.error('[MindOS]', msg);
    return { ok: false, error: msg };
  }

  try {
    if (process.platform === 'win32') writeWindowsShim(cliJs);
    else writeUnixShim(cliJs);
  } catch (e) {
    const msg = e instanceof Error ? e.message : String(e);
    console.error('[MindOS] Failed to write CLI shim:', msg);
    return { ok: false, error: msg };
  }

  const doAppend = opts?.appendPath !== false;
  let appended = false;
  if (doAppend) {
    try {
      appended = appendMindosBinToShellRc();
    } catch (e) {
      console.warn('[MindOS] Could not append PATH to shell rc:', e);
    }
  }

  if (appended) notifyPathAppendedOnce();

  writeUninstallScript();

  console.info('[MindOS] CLI shim:', shimPath(), '→', cliJs);
  return { ok: true, pathAppended: appended };
}

export function buildRefreshCliSuccessDialog(
  platform: NodeJS.Platform,
  zh: boolean,
  pathAppended: boolean,
): Electron.MessageBoxOptions {
  if (platform === 'win32') {
    return {
      type: 'info',
      title: zh ? 'mindos 命令' : 'mindos command',
      message: pathAppended
        ? (zh
            ? `已更新 ${shimPath()}，并已将 ${shimDir()} 加入用户 PATH。请新开终端后运行 mindos --help。`
            : `Updated ${shimPath()} and added ${shimDir()} to your user PATH. Open a new terminal, then run mindos --help.`)
        : (zh
            ? `已更新 ${shimPath()}。如果新终端仍找不到 mindos，请将以下目录加入用户 PATH：`
            : `Updated ${shimPath()}. If a new terminal still cannot find mindos, add this folder to your user PATH:`),
      detail: shimDir(),
    };
  }

  return {
    type: 'info',
    title: zh ? 'mindos 命令' : 'mindos command',
    message: zh
      ? '已更新 ~/.mindos/bin/mindos。若终端仍提示找不到命令，请新开终端窗口，或执行 source ~/.zshrc。'
      : 'Updated ~/.mindos/bin/mindos. Open a new terminal or run source ~/.zshrc if the command is not found.',
    detail: shimPath(),
  };
}

/** Tray / manual: refresh shim + show result (and Windows PATH hint). */
export function refreshMindosCliAndNotify(parent: BrowserWindow | null): void {
  const r = ensureMindosCliShim({ appendPath: true });
  const zh = app.getLocale()?.toLowerCase().startsWith('zh');
  if (r.ok) {
    showMessageBoxSafe(parent, buildRefreshCliSuccessDialog(process.platform, zh, r.pathAppended === true));
    return;
  }
  showMessageBoxSafe(parent, {
    type: 'error',
    title: zh ? 'mindos 安装失败' : 'mindos install failed',
    message: r.error ?? 'Unknown error',
  });
}
